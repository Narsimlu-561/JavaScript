//Arrow function (sum of two numbers)
const sum=(a,b)=>{
  console.log(a+b);
}
console.log(sum(2,5));

//Arrow function (cube of three numbers)
const cube=(n)=>{
  return (n*n*n);
}
console.log(cube(5));

