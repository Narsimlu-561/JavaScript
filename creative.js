let day=prompt("enter a Day in range 1 -> 7");
switch(day){
  case 1:
    alert("monday");
    break;
  case 2:
    alert("Tuesday");
    break;
  case 3:
    alert("Wednessday");
    break;
  case 4:
    alert("Thursday");
    break;
  case 5:
    alert("Friday");
  case 6:
    alert("Saturday");
    break;
  case 7:
    alert("Sunday, funday !");
    break;
  default:
    alert("enter a valid day");
    break;

}